def get_word_age_level(word: str) -> int | None:
    """단어의 적합 연령을 반환합니다.

    Args:
        word: 한국어 단어

    Returns:
        해당 단어가 적합한 최소 연령 (3-7).
        데이터에 없는 단어는 None 반환.

    Examples:
        >>> get_word_age_level("엄마")
        3
        >>> get_word_age_level("학교")
        7
        >>> get_word_age_level("알수없는단어")
        None
    """
    # 누적 어휘를 만들어서 각 연령에서 새로 추가된 단어 확인
    cumulative: set[str] = set()

    for age in sorted(VOCABULARY_BY_AGE.keys()):
        age_vocab = VOCABULARY_BY_AGE[age]
        new_words = age_vocab - cumulative
        if word in new_words:
            return age
        cumulative.update(age_vocab)

    return None


def get_all_vocabulary_up_to_age(age: int) -> set[str]:
    """특정 연령까지의 모든 어휘를 반환합니다.

    Args:
        age: 타깃 연령 (3-7)

    Returns:
        해당 연령까지의 누적 어휘 집합

    Examples:
        >>> vocab = get_all_vocabulary_up_to_age(4)
        >>> "엄마" in vocab  # 3세 어휘
        True
        >>> "학교" in vocab  # 7세 어휘
        False
    """
    result: set[str] = set()
    for vocab_age in sorted(VOCABULARY_BY_AGE.keys()):
        if vocab_age <= age:
            result.update(VOCABULARY_BY_AGE[vocab_age])
    return result
